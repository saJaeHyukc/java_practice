public class Char {

    public static void main(String[] args){
        // 문자 (한 개의 문자 값)
        char a1 = 'a'; // 문자로 표현
        char a2 = 97; // 아스키 코드로 표현
        char a3 = '\u0061'; // 유니코드로 표현

        System.out.println(a1); // a
        System.out.println(a2); // a
        System.out.println(a3); // a
    }
}
